    private void txt_idFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txt_idFocusLost
        // TODO add your handling code here:
        con=konekDB();
        try{
           stm = con.createStatement();
          Rs = stm.executeQuery("SELECT * FROM RESERVATION where ID_transaksi='"+txt_id.getText()+"'");
          Rs.beforeFirst();
          while(Rs.next()){
              txt_nama.setText(Rs.getString("id_user"));      
              txt_masuk.setText(Rs.getString("tgl_masuk"));
              txt_keluar.setText(Rs.getString("tgl_keluar"));
              txt_tempat.setText(Rs.getString("tempat"));
              txt_harga.setText(Rs.getString("harga"));
              txt_admin.setText(Rs.getString("id_admin"));
          }
          
        }catch(SQLException a){
            JOptionPane.showMessageDialog(null, a);
        }
    }//GEN-LAST:event_txt_idFocusLost

