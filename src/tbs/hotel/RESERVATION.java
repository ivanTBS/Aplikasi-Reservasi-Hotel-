    public void tampildataivan(){
         Connection conn=konekDB();
        model = new DefaultTableModel();
        this.jTable1.setModel(model);
        model.addColumn("ID_transaksi");
        model.addColumn("id_user");
        model.addColumn("Tgl_masuk");
        model.addColumn("Tgl_keluar");
        model.addColumn("tempat");
        model.addColumn("harga");
        model.addColumn("id_admin");
         
           
        String id="",nama="",Tgl_in="", Tgl_out="",temp="",har="",id_A="";
      
        try {
            String sql = "select * from reservation";
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            while (rs.next()) {
                id      = rs.getString("ID_transaksi");
                nama    = rs.getString("id_user");
                Tgl_in  = rs.getString("Tgl_masuk");
                Tgl_out = rs.getString("Tgl_keluar");
                temp    = rs.getString("tempat");
                har    = rs.getString("harga");
                id_A    =rs.getString("id_admin");                
                
                String[] tampil = {id,nama,Tgl_in,Tgl_out,temp,har,id_A};
                model.addRow(tampil);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "TERJADI KESALAHAN!! "+e.getMessage());
        }
    }
